GRANT USAGE ON DATABASE LOAN_DB TO ROLE ANALYST;

GRANT USAGE ON SCHEMA PUBLIC TO ROLE DATA_ENGINEER;
GRANT USAGE ON SCHEMA PUBLIC TO ROLE ANALYST;

GRANT ALL PRIVILEGES ON ALL TABLES IN SCHEMA PUBLIC TO ROLE DATA_ENGINEER;
GRANT SELECT ON ALL TABLES IN SCHEMA PUBLIC TO ROLE ANALYST;
GRANT SELECT ON FACT_LOANS TO ROLE FINANCE_ROLE;
GRANT SELECT ON DIM_BORROWER TO ROLE FINANCE_ROLE;
GRANT ROLE ANALYST TO USER naveen;
GRANT ROLE DATA_ENGINEER TO USER naveen;

