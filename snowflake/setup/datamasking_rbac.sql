-- ==========================================================
-- 1. DATA MASKING
-- ==========================================================
CREATE OR REPLACE MASKING POLICY MASK_BORROWER
AS (val STRING) 
RETURNS STRING ->
    CASE
        WHEN CURRENT_ROLE() IN ('ACCOUNTADMIN', 'DATA_ENGINEER') THEN val
        ELSE '*****MASKED*****'
    END;

ALTER TABLE DIM_BORROWER 
MODIFY COLUMN BORROWER 
SET MASKING POLICY MASK_BORROWER;

CREATE OR REPLACE MASKING POLICY MASK_AMOUNT
AS (val FLOAT)
RETURNS FLOAT ->
    CASE
        WHEN CURRENT_ROLE() IN ('ACCOUNTADMIN', 'FINANCE_ROLE') THEN val
        ELSE NULL
    END;

ALTER TABLE FACT_LOANS MODIFY COLUMN ORIGINAL_PRINCIPAL_AMOUNT SET MASKING POLICY MASK_AMOUNT;
ALTER TABLE FACT_LOANS MODIFY COLUMN DISBURSED_AMOUNT SET MASKING POLICY MASK_AMOUNT;
ALTER TABLE FACT_LOANS MODIFY COLUMN REPAID_TO_IBRD SET MASKING POLICY MASK_AMOUNT;

